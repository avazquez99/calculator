class Calculator {
    sum(number1, number2) {
        return number1 + number2;
    }

    subt(number1, number2) {
        return number1 - number2;
    }
    
    mult(number1, number2) {
        return number1 * number2;
    }
    
    div(number1, number2) {
        return number1 / number2;
    }
}